	<h1>WP Disk Usage Plugin Instructions</h1>
	<p>Welcome to the WP Disk Usage plugin! This plugin allows you to monitor and manage the disk usage of your website.</p>

	<h2>Instructions</h2>
	<ol>
		<li>Click the "Gather Results" button in the controls to start gathering disk usage data. This process may take some time depending on the size of your website.</li>
		<li>A progress bar will be displayed showing the current progress of the data gathering process.</li>
		<li>Once the data gathering is complete, the results will be displayed on the Results tab.</li>
		<li>You can take necessary actions based on the disk usage information to optimize your website's storage.</li>
	</ol>
